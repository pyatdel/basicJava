package kr.or.ddit.vo; 

public class SeatVo {
    // SEAT
    private String seatNo;        // 좌석 번호
    private String seatRow;       // 좌석 행
    private String seatCol;       // 좌석 열
    private String seattypeCode;  // 좌석 타입 코드
    private String roomType;      // 상영관 타입


    // SEAT_TYPE
    private String seattypeName;  // 좌석 타입 이름
    private int seattypePrice;    // 좌석 타입 가격

    // RESERVED_SEAT
    private String schNo;         // 스케줄 번호
    private int soldout;          // 0: 예약 완료, 1: 예약 가능

    // Getter and Setter methods
    public String getSeatNo() {
        return seatNo;
    }

    public void setSeatNo(String seatNo) {
        this.seatNo = seatNo;
    }

    public String getSeatRow() {
        return seatRow;
    }

    public void setSeatRow(String seatRow) {
        this.seatRow = seatRow;
    }

    public String getSeatCol() {
        return seatCol;
    }

    public void setSeatCol(String seatCol) {
        this.seatCol = seatCol;
    }

    public String getSeattypeCode() {
        return seattypeCode;
    }

    public void setSeattypeCode(String seattypeCode) {
        this.seattypeCode = seattypeCode;
    }

    public String getRoomType() {
        return roomType;
    }

    public void setRoomType(String roomType) {
        this.roomType = roomType;
    }

    public String getSeattypeName() {
        return seattypeName;
    }

    public void setSeattypeName(String seattypeName) {
        this.seattypeName = seattypeName;
    }

    public int getSeattypePrice() {
        return seattypePrice;
    }

    public void setSeattypePrice(int seattypePrice) {
        this.seattypePrice = seattypePrice;
    }

    public String getSchNo() {
        return schNo;
    }

    public void setSchNo(String schNo) {
        this.schNo = schNo;
    }

    public int getSoldout() {
        return soldout;
    }

    public void setSoldout(int soldout) {
        this.soldout = soldout;
    }
}
